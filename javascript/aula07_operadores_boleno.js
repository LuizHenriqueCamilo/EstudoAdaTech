//Operadores Boleano: Comparações

const numero1 = 10;
const numero2 = 12;

// == ou === Comparação

console.log(numero1 == numero2); // ==, dois iguais compara o valores
console.log(numero1 === numero2); // ===, compara o valor e o tipo
console.log(numero1 == "10"); //obs: recomendavel sempre usa o tres iguais

// comparação de difereça !== ou !===

console.log(numero1 != numero2); // ==, compara o valores
console.log(numero1 !== numero2); //  compara o valor e o tipo
console.log(numero1 != "10"); //obs: recomendavel sempre usar !// == iguais

